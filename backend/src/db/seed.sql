UPDATE venues v
LEFT JOIN (
    SELECT 
        venue_id,
        COUNT(*) AS review_count,
        COALESCE(AVG(rating), 0) AS review_rating
    FROM reviews
    GROUP BY venue_id
) r ON v.id = r.venue_id
SET 
    v.review_count = COALESCE(r.review_count, 0),
    v.review_rating = COALESCE(r.review_rating, 0);